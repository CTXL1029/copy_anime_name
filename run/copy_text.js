/* Copy text into clipboard */

function copyText_1()
{   
    const name_1 = "Nhật ký sống chậm nơi dị giới - Nuôi con trong lúc làm mạo hiểm giả";
    navigator.clipboard.writeText(name_1);
    alert("Đã sao chép tên bộ Anime:\n"+ name_1);
    setTimeout(() => {
        close();
        button.disabled = false;
    }, 2700);
}

